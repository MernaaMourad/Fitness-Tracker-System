    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String time = jTextField1.getText();
        int flag = 0;
        for (int i = 0; i < time.length(); i++) {

            if (time.charAt(i) == '0' || time.charAt(i) == '1' || time.charAt(i) == '2' || time.charAt(i) == '3' || time.charAt(i) == '4' || time.charAt(i) == '5' || time.charAt(i) == '6' || time.charAt(i) == '7' || time.charAt(i) == '8' || time.charAt(i) == '9') {
                flag = 1;
            } else {
                flag = 0;
                break;

            }

        }

        if (time.length() <= 9 && flag == 1) {
            int minutes = Integer.parseInt(jTextField1.getText());

            jLabel7.setText("activity sucessfully added");

            String activity = (String) jComboBox1.getSelectedItem();
            if ((array[0]).getActivity().equals(activity)) {
                array[0].setActivity(activity);
                array[0].setTime(minutes);
            }
            if ((array[1]).getActivity().equals(activity)) {
                array[1].setActivity(activity);
                array[1].setTime(minutes);
            }
            if ((array[2]).getActivity().equals(activity)) {
                array[2].setActivity(activity);
                array[2].setTime(minutes);
            }
            if ((array[3]).getActivity().equals(activity)) {
                array[3].setActivity(activity);
                array[3].setTime(minutes);
            }
        } else {
            jLabel7.setText("error adding the activity ");

        }

        jTextField1.setText("");


    }//GEN-LAST:event_jButton2ActionPerformed

